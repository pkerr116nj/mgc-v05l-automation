instruments_table = Table(
    "instruments",
    metadata,
    Column("instrument_id", Integer, primary_key=True, autoincrement=True),
    Column("ticker", Text, nullable=False),
    Column("cusip", Text, nullable=True),
    Column("asset_class", Text, nullable=False),
    Column("description", Text, nullable=True),
    Column("exchange", Text, nullable=True),
    Column("multiplier", NUMERIC_TYPE, nullable=True),
    Column("is_active", Boolean, nullable=False, default=True),
    UniqueConstraint("ticker", "asset_class", name="uq_instruments_ticker_asset_class"),
    Index("ix_instruments_ticker", "ticker"),
)

bars_table = Table(
    "bars",
    metadata,
    Column("bar_id", Text, primary_key=True),
    Column("instrument_id", Integer, ForeignKey("instruments.instrument_id"), nullable=True, index=True),
    Column("ticker", Text, nullable=False, index=True),
    Column("cusip", Text, nullable=True),
    Column("asset_class", Text, nullable=True),
    Column("data_source", Text, nullable=False, default="internal"),
    Column("timestamp", Text, nullable=False, index=True),
    Column("symbol", Text, nullable=False),
    Column("timeframe", Text, nullable=False),
    Column("start_ts", Text, nullable=False),
    Column("end_ts", Text, nullable=False, index=True),
    Column("open", NUMERIC_TYPE, nullable=False),
    Column("high", NUMERIC_TYPE, nullable=False),
    Column("low", NUMERIC_TYPE, nullable=False),
    Column("close", NUMERIC_TYPE, nullable=False),
    Column("volume", Integer, nullable=False),
    Column("is_final", Boolean, nullable=False),
    Column("session_asia", Boolean, nullable=False),
    Column("session_london", Boolean, nullable=False),
    Column("session_us", Boolean, nullable=False),
    Column("session_allowed", Boolean, nullable=False),
    Column("created_at", Text, nullable=False),
    UniqueConstraint("ticker", "timeframe", "timestamp", "data_source", name="uq_bars_identity"),
    Index("ix_bars_lookup", "ticker", "timeframe", "timestamp"),
)

