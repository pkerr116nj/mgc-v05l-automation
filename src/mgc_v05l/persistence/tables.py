"""SQLite schema for replay-first persistence plus additive research storage."""

from sqlalchemy import (
    Boolean,
    Column,
    ForeignKey,
    Index,
    Integer,
    Numeric,
    Table,
    Text,
    UniqueConstraint,
)

from .db import metadata

NUMERIC_TYPE = Numeric(24, 10, asdecimal=True)

